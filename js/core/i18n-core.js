function t(key, ...args) {
    var dict = I18N[window._LANG] || I18N.es;
    var val = dict && dict[key];
    if (typeof val === 'function') return val(...args);
    return val != null ? val : (I18N.en ? I18N.en[key] : key);
}

function setLang(lang) {
    window._LANG = lang === 'en' ? 'en' : 'es';
    try { localStorage.setItem('sigmaPro_lang', window._LANG); } catch(e) {}
    var pages = ['auditoria', 'usuarios', 'dispositivos', 'ml'];
    if (pages.indexOf(currentPage) >= 0 && typeof loadPage === 'function') {
        loadPage(currentPage);
    }
}

